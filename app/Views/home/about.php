<section class="container page-content">
    <h2>Sobre o TicketMaster</h2>

    <p>
        O <strong>TicketMaster</strong> é um sistema de vendas de ingressos para cinema desenvolvido
        como projeto acadêmico. Nossa missão é tornar a experiência de compra de ingressos
        mais simples, rápida e segura.
    </p>

    <h3>Funcionalidades</h3>
    <ul>
        <li>Cadastro e login de usuários com senha criptografada</li>
        <li>Recuperação de senha por e-mail</li>
        <li>Listagem e busca de filmes</li>
        <li>Visualização de sessões disponíveis</li>
        <li>Compra e cancelamento de ingressos</li>
        <li>Avaliações e comentários de filmes</li>
    </ul>

    <h3>Tecnologias Utilizadas</h3>
    <ul>
        <li>PHP 8 — padrão MVC com POO</li>
        <li>MySQL — banco de dados relacional via PDO</li>
        <li>HTML5 semântico + CSS3</li>
        <li>Sessões, cookies e proteção CSRF</li>
    </ul>

